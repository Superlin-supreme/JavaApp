    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String ip=jTextField1.getText();//取出IP地址数据

        String port=jTextField2.getText();//取出端口号数据

        try {
            uc = new UDPClient(ip,port);//发起UDP连接
            jTextArea1.append("UDP连接创建成功.\r\n");
            Thread receiver = new Thread(){
                @Override
                public void run(){
                    String msg = null;
                    while( (msg=uc.receive()) != null){
                        jTextArea1.append(msg+"\n");
                    }
                    jTextArea1.append("对话已关闭！\n");
                }
            };
            receiver.start();

        } catch (IOException ex) {

            jTextArea1.append("服务器连接失败.\r\n");

        } 
    }//GEN-LAST:event_jButton1ActionPerformed

